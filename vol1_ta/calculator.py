def add(a,b):
    """
    Add two numbers and return the result.

    Parameters:
        a (float) -- the first number
        b (float) -- the second number

    Returns:
        The sum of a and b.
    """
    return a + b

def multiply(a,b):
    """
    Add two numbers and return the result.

    Parameters:
        a (float) -- the first number
        b (float) -- the second number

    Returns:
        The product of a and b.
    """
    return a*b

def sqrt(a):
    """
    Return the square root of a real number.

    Parameters:
        a (float) -- the input number

    Returns:
        The square root of a.
    """
    return a**.5
